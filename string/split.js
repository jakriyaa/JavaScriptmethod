// Strign কে Arry তে রুপারন্তর করতে (Split) ব্যবহার করা হয়। যেমনঃ
const i ="Jakariya, Hamja, Ebrahim, Tamanna, Mousomi";
const u=i.split(",");
console.log(u);


// আবার Arry কে String এ রুপান্তর করতে হলে(JOIN) ব্যবহার করা হয়।যেমনঃ
const poribar =["Jakariya, Hamja, Ebrahim, Tamanna, Mousomi"]
console.log(poribar.join(",")) 
// Join এর ক্ষেত্রে (| -,) এগুলো দিয়ে Join করা যায়।